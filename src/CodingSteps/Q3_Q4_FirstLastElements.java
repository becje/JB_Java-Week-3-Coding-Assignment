package CodingSteps;

public class Q3_Q4_FirstLastElements {

	public static void main(String[] args) {
		
		String[] names = new String[6];		
		names[0] = "Sam";
		names[1] = "Tommy";
		names[2] = "Tim";
		names[3] = "Sally";
		names[4] = "Buck";
		names[5] = "Bob";
		
		System.out.println("First element of an array is: " + names[0]);
		System.out.println("Last element of an array is: " + names[names.length-1]);

	}

}
